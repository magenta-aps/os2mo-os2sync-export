@pytest.mark.integration_test
async def test_event_inactive_org_unit(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    create_org_unit: TestingOrgUnitCreateOrgUnitCreate,
) -> None:
    # Ensure that the event queue is empty after the create event has been processed
    async for attempt in retrying(stop=stop_before_delay(60)):
        with attempt:
            assert (
                await get_pending_org_unit_events(
                    graphql_client, str(create_org_unit.uuid)
                )
                == []
            )

    # Act
    # Terminate the org unit so it is no longer active in MO.
    await graphql_client.testing__org_unit_terminate(
        input=OrganisationUnitTerminateInput(
            uuid=create_org_unit.uuid,
            to=datetime(2025, 1, 1, 0, 0, 0),
        )
    )

    # Enqueue an internal org_unit sync event for the now-inactive unit. Unlike
    # the event MO derives from the terminate (which is created asynchronously),
    # event_send creates the event synchronously, so it is guaranteed to be
    # pending right after this call. This makes the assertion below unambiguous:
    # an empty queue can only mean the event was processed, not that it hasn't
    # been emitted yet.
    await graphql_client.event_send(
        namespace="os2sync_export",
        routing_key="org_unit",
        subject=str(create_org_unit.uuid),
    )

    # The event must be processed even though the org unit is no longer active.
    # Before the fix the NotFoundError raised by sync_orgunit crashed the
    # /sync/org_unit handler, so the event was never acknowledged and stayed
    # stuck in the queue forever.
    async for attempt in retrying(stop=stop_before_delay(60)):
        with attempt:
            assert (
                await get_pending_org_unit_events(
                    graphql_client, str(create_org_unit.uuid)
                )
                == []
            )
