async def get_pending_org_unit_events(
    graphql_client: GraphQLClient, subject: str
) -> list[dict]:
    response = await graphql_client.execute(
        query=gql("""
            query PendingOrgUnitEvents($subject: [String!]) {
              events(filter: {subjects: $subject}) {
                objects {
                  subject
                }
              }
            }
        """),
        variables={"subject": [subject]},
    )
    data = graphql_client.get_data(response)
    return data["events"]["objects"]


